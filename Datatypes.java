
// UNDERSTANDING THE DATATYPES IN  JAVA 



public class Datatypes {

    public static void main(String[] args) {
        int a = 9;
        byte by = 127;
        short sh = 558;
        long l = 58541l;  // whenever you mention a long value always put l at the end
         

        float f = 5.208f; // whenever you mention a long value always put f at the end
        double d = 20.56;


        char c = 's'; // all the character are mentioned in single quotes ' ';
        boolean b = true; // boolean returns only true or false
        System.out.println(a);
        System.out.println(by);
        System.out.println(sh);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);

    }
    
}
