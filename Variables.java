 // simple program to simplify the use of datatypes and use of variables 



public class Variables {
   public static void main(String[] args) {
    int a = 2;  // datatype is integer , data or value ie 2 is stored in variable a
    int b = 3;  // datatype is integer , data or value ie 3 is stored in variable b
    int sum = a+b;  // the sum is the variable that stores the value of the sum of a+b
    System.out.println("The sum of the numbers is:"); // use of System.out.println to print on a new line 
    System.out.print(sum);   // use of System.out.print to print the sum 
   }
}



